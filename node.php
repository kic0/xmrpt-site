<br>
Para executar o programa do "nó" de Monero necessita de:<br>
Estar conectado à Internet (para receber e transmitir transações)<br>
Fazer o download do blockchain do Monero (que tem aproximadamente 60 gigabytes e sempre a crescer)<br>
Então, se  executar a carteira oficial do Monero no seu computador, cada um pode ser um nó! <br>
